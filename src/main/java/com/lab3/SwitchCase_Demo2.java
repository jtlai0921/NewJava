package com.lab3;

public class SwitchCase_Demo2 {
    public static void main(String[] args) {
        char a = 'A'; // ASCII 65
        switch(a) {
            case 'A': // 65
                System.out.println("得到 A");
                break;
            case 'B': // 66
                System.out.println("得到 B");
                break;
        }
    }
}
